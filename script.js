function createShader(gl, type, source) {
  const shader = gl.createShader(type);
  gl.shaderSource(shader, source);
  gl.compileShader(shader);
  if (!gl.getShaderParameter(shader, gl.COMPILE_STATUS)) {
    throw new Error(gl.getShaderInfoLog(shader));
  }
  return shader;
}

function createProgram(gl, vertexSource, fragmentSource) {
  const program = gl.createProgram();
  gl.attachShader(program, createShader(gl, gl.VERTEX_SHADER, vertexSource));
  gl.attachShader(program, createShader(gl, gl.FRAGMENT_SHADER, fragmentSource));
  gl.linkProgram(program);
  if (!gl.getProgramParameter(program, gl.LINK_STATUS)) {
    throw new Error(gl.getProgramInfoLog(program));
  }
  return program;
}

function initRenderCanvas() {
  const canvas = document.querySelector("#renderCanvas");
  const gl = canvas.getContext("webgl", {
    alpha: true,
    antialias: true,
    depth: false,
    preserveDrawingBuffer: true
  });

  if (!gl || prefersReducedMotion) return;

  const vertexSource = `
    attribute vec3 aPosition;
    attribute float aSeed;
    attribute float aKind;
    attribute vec3 aColor;

    uniform float uTime;
    uniform vec2 uMouse;
    uniform float uAspect;

    varying float vAlpha;
    varying float vKind;
    varying vec3 vColor;

    mat3 rotateX(float a) {
      float s = sin(a);
      float c = cos(a);
      return mat3(1.0, 0.0, 0.0, 0.0, c, -s, 0.0, s, c);
    }

    mat3 rotateY(float a) {
      float s = sin(a);
      float c = cos(a);
      return mat3(c, 0.0, s, 0.0, 1.0, 0.0, -s, 0.0, c);
    }

    void main() {
      vec3 p = aPosition;
      float drift = sin(uTime * 0.75 + aSeed * 6.28318) * 0.045;
      p *= 1.0 + drift * (1.0 - step(0.5, aKind));
      p.xy += vec2(uMouse.x, -uMouse.y) * 0.1 * (0.55 + aKind);
      p = rotateY(uTime * 0.14 + uMouse.x * 0.5) * rotateX(uMouse.y * 0.34) * p;

      float depth = 3.35 + p.z;
      vec2 projected = p.xy / depth * 1.95;
      projected.x /= uAspect;
      projected += vec2(0.26, 0.02);

      gl_Position = vec4(projected, (p.z + 2.2) / 5.8, 1.0);
      gl_PointSize = mix(3.7, 1.55, aKind) * (3.4 / depth) + sin(uTime * 2.0 + aSeed) * 0.55;

      vKind = aKind;
      vColor = aColor;
      vAlpha = mix(0.92, 0.38, aKind) * (0.75 + 0.25 * sin(uTime + aSeed * 12.0));
    }
  `;

  const fragmentSource = `
    precision mediump float;
    varying float vAlpha;
    varying float vKind;
    varying vec3 vColor;

    void main() {
      vec2 uv = gl_PointCoord - 0.5;
      float d = length(uv);
      float edge = smoothstep(0.5, 0.04, d);
      float core = smoothstep(0.16, 0.0, d);
      vec3 color = vColor + core * 0.35;
      gl_FragColor = vec4(color, edge * vAlpha);
    }
  `;

  let program;
  try {
    program = createProgram(gl, vertexSource, fragmentSource);
  } catch (error) {
    console.warn(error);
    return;
  }

  const stride = 8;
  const data = [];
  const cyan = [0.2, 0.9, 0.95];
  const mint = [0.66, 0.94, 0.77];
  const amber = [1.0, 0.74, 0.36];
  const rose = [1.0, 0.44, 0.57];

  for (let i = 0; i < 1500; i += 1) {
    const u = Math.random();
    const v = Math.random();
    const theta = u * Math.PI * 2;
    const phi = Math.acos(2 * v - 1);
    const radius = 0.76 + Math.random() * 0.24;
    const color = Math.random() > 0.84 ? amber : Math.random() > 0.68 ? mint : cyan;
    data.push(
      Math.sin(phi) * Math.cos(theta) * radius,
      Math.cos(phi) * radius,
      Math.sin(phi) * Math.sin(theta) * radius,
      Math.random(),
      0,
      color[0],
      color[1],
      color[2]
    );
  }

  for (let i = 0; i < 720; i += 1) {
    const color = Math.random() > 0.8 ? rose : Math.random() > 0.55 ? amber : cyan;
    data.push(
      (Math.random() - 0.45) * 6.3,
      (Math.random() - 0.5) * 3.4,
      (Math.random() - 0.5) * 2.6,
      Math.random(),
      1,
      color[0],
      color[1],
      color[2]
    );
  }

  const buffer = gl.createBuffer();
  const values = new Float32Array(data);
  const count = values.length / stride;

  const locations = {
    aPosition: gl.getAttribLocation(program, "aPosition"),
    aSeed: gl.getAttribLocation(program, "aSeed"),
    aKind: gl.getAttribLocation(program, "aKind"),
    aColor: gl.getAttribLocation(program, "aColor"),
    uTime: gl.getUniformLocation(program, "uTime"),
    uMouse: gl.getUniformLocation(program, "uMouse"),
    uAspect: gl.getUniformLocation(program, "uAspect")
  };

  function resize() {
    const ratio = Math.min(window.devicePixelRatio || 1, 2);
    const width = canvas.clientWidth || window.innerWidth;
    const height = canvas.clientHeight || window.innerHeight;
    canvas.width = Math.max(1, Math.floor(width * ratio));
    canvas.height = Math.max(1, Math.floor(height * ratio));
    gl.viewport(0, 0, canvas.width, canvas.height);
  }

  function draw(time) {
    gl.clearColor(0, 0, 0, 0);
    gl.clear(gl.COLOR_BUFFER_BIT);
    gl.enable(gl.BLEND);
    gl.blendFunc(gl.SRC_ALPHA, gl.ONE);
    gl.useProgram(program);
    gl.bindBuffer(gl.ARRAY_BUFFER, buffer);
    gl.bufferData(gl.ARRAY_BUFFER, values, gl.STATIC_DRAW);

    gl.enableVertexAttribArray(locations.aPosition);
    gl.vertexAttribPointer(locations.aPosition, 3, gl.FLOAT, false, stride * 4, 0);
    gl.enableVertexAttribArray(locations.aSeed);
    gl.vertexAttribPointer(locations.aSeed, 1, gl.FLOAT, false, stride * 4, 3 * 4);
    gl.enableVertexAttribArray(locations.aKind);
    gl.vertexAttribPointer(locations.aKind, 1, gl.FLOAT, false, stride * 4, 4 * 4);
    gl.enableVertexAttribArray(locations.aColor);
    gl.vertexAttribPointer(locations.aColor, 3, gl.FLOAT, false, stride * 4, 5 * 4);

    gl.uniform1f(locations.uTime, time * 0.001);
    gl.uniform2f(locations.uMouse, easedMouse.x, easedMouse.y);
    gl.uniform1f(locations.uAspect, canvas.width / canvas.height);
    gl.drawArrays(gl.POINTS, 0, count);
    signalFrame = requestAnimationFrame(draw);
  }

  resize();
  window.addEventListener("resize", resize);
  draw(0);
}

function initSignalCanvas() {
  const canvas = document.querySelector("#signalCanvas");
  const context = canvas.getContext("2d");
  if (prefersReducedMotion) return;

  let particles = [];

  function resize() {
    const ratio = Math.min(window.devicePixelRatio || 1, 2);
    canvas.width = Math.floor(window.innerWidth * ratio);
    canvas.height = Math.floor(window.innerHeight * ratio);
    canvas.style.width = `${window.innerWidth}px`;
    canvas.style.height = `${window.innerHeight}px`;
    context.setTransform(ratio, 0, 0, ratio, 0, 0);

    const count = Math.min(92, Math.max(42, Math.floor(window.innerWidth / 17)));
    particles = Array.from({ length: count }, () => ({
      x: Math.random() * window.innerWidth,
      y: Math.random() * window.innerHeight,
      vx: (Math.random() - 0.5) * 0.34,
      vy: (Math.random() - 0.5) * 0.34,
      size: 0.9 + Math.random() * 1.5,
      hue: Math.random() > 0.76 ? "255, 188, 94" : "53, 228, 242"
    }));
  }

  function draw() {
    context.clearRect(0, 0, window.innerWidth, window.innerHeight);
    const pointer = {
      x: window.innerWidth * (0.5 + easedMouse.x * 0.36),
      y: window.innerHeight * (0.5 + easedMouse.y * 0.36)
    };

    particles.forEach((particle, index) => {
      const dx = pointer.x - particle.x;
      const dy = pointer.y - particle.y;
      const distance = Math.hypot(dx, dy);

      if (distance < 210) {
        particle.x -= dx * 0.0017;
        particle.y -= dy * 0.0017;
      }

      particle.x += particle.vx;
      particle.y += particle.vy;

      if (particle.x < -20) particle.x = window.innerWidth + 20;
      if (particle.x > window.innerWidth + 20) particle.x = -20;
      if (particle.y < -20) particle.y = window.innerHeight + 20;
      if (particle.y > window.innerHeight + 20) particle.y = -20;

      context.beginPath();
      context.arc(particle.x, particle.y, particle.size, 0, Math.PI * 2);
      context.fillStyle = `rgba(${particle.hue}, 0.56)`;
      context.fill();

      for (let nextIndex = index + 1; nextIndex < particles.length; nextIndex += 1) {
        const next = particles[nextIndex];
        const gap = Math.hypot(particle.x - next.x, particle.y - next.y);
        if (gap < 118) {
          context.beginPath();
          context.moveTo(particle.x, particle.y);
          context.lineTo(next.x, next.y);
          context.strokeStyle = `rgba(168, 239, 197, ${0.12 * (1 - gap / 118)})`;
          context.lineWidth = 1;
          context.stroke();
        }
      }
    });

    requestAnimationFrame(draw);
  }

  resize();
  window.addEventListener("resize", resize);
  draw();
}

